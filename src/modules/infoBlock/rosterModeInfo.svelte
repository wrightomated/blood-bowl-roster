<h3>Game Modes</h3>
<h4>League Mode</h4>
<p>
    League mode is the core Blood Bowl 2020 experience, where coaches manage
    their team through an ongoing campaign. Your team will gain SPP (Star Player
    Points), develop new skills, suffer injuries, and manage their treasury
    between matches. Teams start with a budget and must carefully balance their
    roster development, player health, and financial management throughout the
    season. This mode features:
</p>
<ul>
    <li>Full team progression and development</li>
    <li>Spiraling Expenses for high-value teams</li>
    <li>Management of team staff, re-rolls, and fan factor</li>
    <li>Player retirement and journeymen rules</li>
    <li>Comprehensive injury and casualty tables</li>
</ul>
<h4>Exhibition Mode</h4>
<p>
    Exhibition mode offers one-off matches perfect for casual play or tournament
    settings. Teams are built to a predetermined budget without ongoing
    progression. This mode is ideal for:
</p>
<ul>
    <li>Learning the game mechanics</li>
    <li>Testing new team compositions</li>
    <li>Quick matches without long-term consequences</li>
    <li>Tournament play</li>
</ul>
