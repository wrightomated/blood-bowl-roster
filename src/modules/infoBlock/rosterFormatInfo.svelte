<h3>Game Formats</h3>
<h4>Elevens (Classic Blood Bowl)</h4>

<p>
    The standard Blood Bowl 2020 format played with 11 players per side. This is
    the complete game experience featuring:
</p>
<ul>
    <li>Full team rosters of up to 16 players</li>
    <li>All standard rules and special plays</li>
    <li>Complete range of player positions and skills</li>
    <li>Standard pitch size (26 squares long by 15 squares wide)</li>
</ul>
<p>Rules found in <em>Blood Bowl - The Official Rules</em></p>

<h4>Sevens</h4>

<p>
    Blood Bowl Sevens is an official, streamlined version of the game designed
    for faster play. Key features include:
</p>
<ul>
    <li>7 players per side on the pitch</li>
    <li>Smaller roster size (11 players maximum)</li>
    <li>Reduced pitch size (16 squares long by 11 squares wide)</li>
    <li>Games typically last 45-60 minutes</li>
    <li>Simplified rules while maintaining core mechanics</li>
    <li>Perfect for weeknight leagues or lunch break games</li>
</ul>
<p>Rules found in <em>Blood Bowl: Death Zone</em></p>

<h4>Dungeon Bowl</h4>

<p>
    The underground variant of Blood Bowl where college teams compete in
    maze-like dungeons. Unique features include:
</p>
<ul>
    <li>Teams organized by College of Magic</li>
    <li>Dungeon layout with corridors and rooms instead of a pitch</li>
    <li>Special rules for doors, chests, and teleporters</li>
    <li>
        Victory achieved by scoring with the ball found in one of several chests
    </li>
    <li>Additional hazards and traps in the dungeon environment</li>
    <li>Team composition rules specific to magical colleges</li>
</ul>
<p>
    Rules found in <em
        >Dungeon Bowl: The Game of Subterranean Blood Bowl Mayhem</em
    >
</p>

<h4>Gutter Bowl</h4>

<p>
    The street variant of Blood Bowl played in the back alleys of major cities.
    Features include:
</p>

<ul>
    <li>Irregular playing area with urban obstacles</li>
    <li>Environmental hazards like sewers and scaffolding</li>
    <li>Special rules for terrain interaction</li>
    <li>Modified scoring rules for different "street touchdown" zones</li>
    <li>Unique special plays representing street tactics</li>
    <li>Perfect for narrative campaigns in urban settings</li>
</ul>
<p>Rules found in <em>Blood Bowl: Gutter Bowl</em></p>

<h4>Beach Bowl</h4>

<p>
    Beach Bowl is a Blood Bowl variant designed to simulate
    low-stakes games of oceanside sand football. Key features include:
</p>
<ul>
    <li>7 players per side on the pitch</li>
    <li>Smaller roster size (11 players maximum)</li>
    <li>Reduced pitch size (20 squares long by 9 squares wide)</li>
    <li>No casualties</li>
</ul>
<p>Rules found on the NAF website</p>

