<h3>Draft</h3>
<p>Draft mode is before you've played your first match.</p>
<ul>
    <li>Removing anything from your roster will give you a refund.</li>
    <li>Rerolls are the price stated in your roster.</li>
    <li>Dedicated fans are 10k to increase.</li>
</ul>

<h3>Commenced</h3>
<p>The league has started and you have played at least one match.</p>
<ul>
    <li>Removing anything from your roster will not give you a refund.</li>
    <li>Rerolls are double the price.</li>
    <li>
        Dedicated fans should only be changed depending on game result and you
        shouldn't purchase with money.
    </li>
</ul>
